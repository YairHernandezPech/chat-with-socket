function modoclaro() {
  document.querySelector(".body").style = "background-color: #f4f7f6;";
  document.querySelector(".nombre").style = "color: black;";
  document.querySelector(".activo").style = "color: black;";
}
function modooscuro() {
  document.querySelector(".body").style =
    "background: #0F2027;background: -webkit-linear-gradient(to right, #2C5364, #203A43, #0F2027);background: linear-gradient(to right, #2C5364, #203A43, #0F2027);";
  document.querySelector(".nombre").style = "color: white;";
  document.querySelector(".activo").style = "color: white;";
}
